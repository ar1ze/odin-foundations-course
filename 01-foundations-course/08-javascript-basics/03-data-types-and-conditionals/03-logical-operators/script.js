// Logical operotrs OR AND NOT
console.log(`"true || true" is: ${true ||  true}`);
console.log(`"false || true" is: ${false || true}`);

let hour = 9;
if (hour < 10 || hour > 18) {
  console.log(`The time is ${hour}. The office closed.`);
}

// && AND
console.log(`true && true" is: ${true && true}`);
console.log(`false && true" is: ${false && true}`);

// ! NOT
console.log(`"!true" results in: ${!true}`);
console.log(`"!0" results in: ${!0}`);
console.log(undefined || null || 0);